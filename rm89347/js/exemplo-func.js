// Função Padrão
function adicao(a,b){
    return a+b
}
// Executar a função a apresentar o valor.
console.log("Resultado da Adição: " + adicao(10,10))

// Arrow-function
const subtracao = (c,d) => c-d

// Executar a função a apresentar o valor.
console.log("Resultado da Subitração: " + subtracao(30,10))